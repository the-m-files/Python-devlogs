# // this is floor, which is basically division but cutting off any decimals
print(3//1)

# ** this is exponent, this raises any two numbers to a power --> 2 ** 4; 2 --> base and 4 --> power
print(2**4)

# % this is the modules sign which makes it so that it gives you the remainder of a division function
print(15 % 4)

